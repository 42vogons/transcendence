import TwoFAInput from '@/components/TwoFAInput'
import Loading from '@/components/loading'
import { UserContext } from '@/contexts/UserContext'
import { api } from '@/services/api'
import { AuthContainer } from '@/styles/pages/auth'
import Head from 'next/head'

import { useRouter } from 'next/router'
import { useContext, useEffect, useState } from 'react'
import { toast } from 'react-toastify'

export default function Auth() {
	const { handleLogin } = useContext(UserContext)

	const router = useRouter()

	const [isLoading, setIsLoading] = useState(false)
	const [request2FA, setRequest2FA] = useState(false)

	console.log(router.query.code)

	useEffect(() => {
		async function authUser(code: string) {
			setIsLoading(true)
			try {
				const { data } = await api.post('/auth/user', { code })
				toast('Login was successful', { type: 'success' })
				const { action, user } = data
				if (action === 'authenticate') {
					setRequest2FA(true)
					setIsLoading(false)
				}
				if (action === 'logged') {
					handleLogin(user)
					router.push('/')
				}
			} catch (error) {
				console.log('error:', error)
				toast('An error occurred during authentication', {
					type: 'error',
				})
				router.push('/login')
				setIsLoading(false)
			}
		}
		const code = router.query.code as string
		console.log('code:', code)
		if (code) {
			authUser(code)
		}
		// eslint-disable-next-line react-hooks/exhaustive-deps
	}, [router])

	async function sendCode(twoFAcode: string) {
		setIsLoading(true)
		setRequest2FA(false)
		try {
			const res = await api.post('/checkTwoFactor', { code: twoFAcode })
			toast('Login was successful', { type: 'success' })
			console.log('res:', res)
			const { action } = res.data
			if (action === 'logged') {
				console.log('action login:', action)
				// todo adicionar user no 2fa
				router.push('/')
			} else {
				console.log('erro action:', action)
			}
		} catch (error) {
			console.log('error:', error)
			toast('An error occurred during authentication', {
				type: 'error',
			})
			// router.push('/login')
			setIsLoading(false)
			setRequest2FA(true)
		}
	}

	return (
		<>
			<Head>
				<title>Transcendence - Pong</title>
				<meta
					name="description"
					content="Generated by create next app"
				/>
				<meta
					name="viewport"
					content="width=device-width, initial-scale=1"
				/>
				<link rel="icon" href="/favicon.ico" />
			</Head>
			<AuthContainer>
				{isLoading && <Loading size={200} />}
				{request2FA && <TwoFAInput sendTwoFA={sendCode} />}

				{/* <button
					onClick={() => {
						toast('Authenticated', { type: 'success' })
					}}
				>
					Toast
				</button> */}
			</AuthContainer>
		</>
	)
}
